/**
 * Reads src/data/aPlusTerms.source.txt and writes src/data/aPlusGlossaryGenerated.ts
 * — flashcards + PBQ match scenarios for CompTIA A+ glossary.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const sourcePath = path.join(root, 'src', 'data', 'aPlusTerms.source.txt')
const outPath = path.join(root, 'src', 'data', 'aPlusGlossaryGenerated.ts')

const text = fs.readFileSync(sourcePath, 'utf8')

function truncate(s, max) {
  const t = s.replace(/\s+/g, ' ').trim()
  if (t.length <= max) return t
  return t.slice(0, max - 1).trim() + '…'
}

/** Split glossary into blocks separated by blank lines */
function parseBlocks(raw) {
  const blocks = raw.split(/\r?\n\r?\n+/).map((b) => b.trim()).filter(Boolean)
  const entries = []
  const seen = new Set()

  for (const block of blocks) {
    const lines = block.split(/\r?\n/).map((l) => l.trim()).filter((l) => l.length > 0)
    if (lines.length === 0) continue

    const term = lines[0]
    let expansion = ''
    let definition = ''

    if (lines.length >= 3) {
      expansion = lines[1]
      definition = lines.slice(2).join('\n')
    } else if (lines.length === 2) {
      definition = lines[1]
    } else {
      definition = ''
    }

    const key = term.toLowerCase()
    if (seen.has(key)) continue
    seen.add(key)

    entries.push({ term, expansion: expansion.replace(/\s+/g, ' ').trim(), definition: definition.replace(/\s+/g, ' ').trim() })
  }

  return entries
}

const entries = parseBlocks(text)

const flashcards = entries.map((e, index) => {
  const id = `aplus-gloss-${String(index).padStart(4, '0')}`
  const hasExpansion = e.expansion.length > 0
  const front = hasExpansion
    ? `What does "${e.term}" stand for?`
    : `Define: ${e.term}`
  const backParts = []
  if (hasExpansion) backParts.push(e.expansion)
  if (e.definition) backParts.push(e.definition)
  const back = truncate(backParts.join(' — '), 720)

  return {
    id,
    certification: 'a-plus',
    deck: 'A+ Core Terms',
    topic: 'Glossary',
    type: 'term',
    front,
    back,
    explanation: e.definition ? truncate(e.definition, 400) : undefined,
    tags: ['A+', 'Glossary'],
  }
})

/** PBQ: acronym → expansion; allow final batch of 3–5 */
const matchable = entries.filter(
  (e) =>
    e.expansion.length >= 3 &&
    e.term.length <= 44 &&
    e.expansion.length <= 84,
)

const pbqScenarios = []
let batchIndex = 0
let offset = 0
while (offset < matchable.length) {
  const remaining = matchable.length - offset
  let size = 5
  if (remaining < 5) {
    if (remaining >= 3) size = remaining
    else break
  }
  const chunk = matchable.slice(offset, offset + size)
  offset += size

  const scenarioId = `pbq-aplus-gloss-${String(batchIndex).padStart(3, '0')}`
  const items = chunk.map((e, j) => ({
    id: `${scenarioId}-i${j}`,
    label: truncate(e.term, 44),
  }))
  const targets = chunk.map((e, j) => ({
    id: `${scenarioId}-t${j}`,
    label: truncate(e.expansion, 62),
  }))

  const solution = {}
  for (let j = 0; j < chunk.length; j += 1) {
    solution[items[j].id] = targets[j].id
  }

  pbqScenarios.push({
    id: scenarioId,
    certification: 'a-plus',
    topic: 'Glossary',
    difficulty: 'easy',
    type: 'match',
    prompt: 'Match each term or acronym to its correct expansion.',
    instructions: 'Drag each label from the pool onto the matching expansion.',
    items,
    targets,
    solution,
    explanation: 'Each pair comes from the A+ glossary: match the shorthand to its full written form.',
  })
  batchIndex += 1
}

const header = `/**
 * AUTO-GENERATED by scripts/generate-a-plus-glossary.mjs — do not edit by hand.
 * Source: src/data/aPlusTerms.source.txt
 */

`

const flashOut = `${header}export const aPlusGlossaryFlashcards = ${JSON.stringify(flashcards, null, 2)} as const

export const aPlusGlossaryMatchScenarios = ${JSON.stringify(pbqScenarios, null, 2)} as const
`

fs.writeFileSync(outPath, flashOut, 'utf8')

console.log(`Wrote ${flashcards.length} flashcards, ${pbqScenarios.length} PBQ match scenarios → ${path.relative(root, outPath)}`)
